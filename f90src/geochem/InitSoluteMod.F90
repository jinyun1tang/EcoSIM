  S0=AAL1+ASO41+DPALS
  S1=S0**2-4.0*(AAL1*ASO41-DPALS*AALS1)
  RALS=TSL*(S0-SQRT(S1))
  S0=AFE1+AOH1+DPFE1
  S1=S0**2-4.0*(AFE1*AOH1-DPFE1*AFEO1)
  RFEO1=TSL*(S0-SQRT(S1))
  S0=AFEO1+AOH1+DPFE2
  S1=S0**2-4.0*(AFEO1*AOH1-DPFE2*AFEO2)
  RFEO2=TSL*(S0-SQRT(S1))
  S0=AFEO2+AOH1+DPFE3
  S1=S0**2-4.0*(AFEO2*AOH1-DPFE3*AFEO3)
