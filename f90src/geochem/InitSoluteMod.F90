!     CALCITE
!
    PX=AMAX1(ACO31,AHCO31,ACO21)
    R1=AHY1
    P1=ACA1
    IF(test_aeqb(PX,ACO31))THEN
      P2=ACO31
      NR1=0
      SP=SPCAC
    ELSEIF(test_aeqb(PX,AHCO31))THEN
      P2=AHCO31
      NR1=1
      SP=SHCAC1
    ELSEIF(test_aeqb(PX,ACO21))THEN
      P2=ACO21
      NR1=2
      SP=SHCAC2
    ENDIF
    RHCAC3=0._r8
    RHCACH=0._r8
    RHCACO=0._r8
    R1=AMAX1(ZERO,R1)
    P1=AMAX1(ZERO,P1)
    P2=AMAX1(ZERO,P2)
    SPX=SP*R1**NR1
    S0=P1+P2
    S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
    RPCACX=AMAX1(-PCACO1,TPD*(S0-SQRT(S1)))
    IF(test_aeqb(PX,ACO31))THEN
      RHCAC3=RPCACX
    ELSEIF(test_aeqb(PX,AHCO31))THEN
      RHCACH=RPCACX
    ELSEIF(test_aeqb(PX,ACO21))THEN
      RHCACO=RPCACX
    ENDIF
!
!     GYPSUM
!
    P1=ACA1
    P2=ASO41
    P1=AMAX1(ZERO,P1)
    P2=AMAX1(ZERO,P2)
    SPX=SPCAS
    S0=P1+P2
    S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
    RPCASO=AMAX1(-PCASO1,TPD*(S0-SQRT(S1)))
!
!     PHOSPHORUS PRECIPITATION-DISSOLUTION IN NON-BAND SOIL ZONE
!
!
!     ALUMINUM PHOSPHATE (VARISCITE)
!
    PX=AMAX1(AAL1,AALO1,AALO2,AALO3,AALO4)
    PY=AMAX1(AH1P1,AH2P1)
    R1=AHY1
    P3=AHY1
    IF(test_aeqb(PY,AH1P1))THEN
      P2=AH1P1
      IF(test_aeqb(PX,AAL1))THEN
        P1=AAL1
        NR1=1
        NP3=0
        SP=SHA0P1
      ELSEIF(test_aeqb(PX,AALO1))THEN
        P1=AALO1
        NR1=0
        NP3=0
        SP=SPA1P1
      ELSEIF(test_aeqb(PX,AALO2))THEN
        P1=AALO2
        NR1=0
        NP3=1
        SP=SHA2P1
      ELSEIF(test_aeqb(PX,AALO3))THEN
        P1=AALO3
        NR1=0
        NP3=2
        SP=SHA3P1
      ELSEIF(test_aeqb(PX,AALO4))THEN
        P1=AALO4
        NR1=0
        NP3=3
        SP=SHA4P1
      ENDIF
    ELSE
      P2=AH2P1
      IF(test_aeqb(PX,AAL1))THEN
        P1=AAL1
        NR1=2
        NP3=0
        SP=SHA0P2
      ELSEIF(test_aeqb(PX,AALO1))THEN
        P1=AALO1
        NR1=1
        NP3=0
        SP=SHA1P2
      ELSEIF(test_aeqb(PX,AALO2))THEN
        P1=AALO2
        NR1=0
        NP3=0
        SP=SPA2P2
      ELSEIF(test_aeqb(PX,AALO3))THEN
        P1=AALO3
        NR1=0
        NP3=1
        SP=SHA3P2
      ELSEIF(test_aeqb(PX,AALO4))THEN
        P1=AALO4
        NR1=0
        NP3=2
        SP=SHA4P2
      ENDIF
    ENDIF
    RHA0P1=0._r8
    RHA1P1=0._r8
    RHA2P1=0._r8
    RHA3P1=0._r8
    RHA4P1=0._r8
    RHA0P2=0._r8
    RHA1P2=0._r8
    RHA2P2=0._r8
    RHA3P2=0._r8
    RHA4P2=0._r8
    R1=AMAX1(ZERO,R1)
    P1=AMAX1(ZERO,P1)
    P2=AMAX1(ZERO,P2)
    P3=AMAX1(ZERO,P3)
    SPX=SP*R1**NR1/P3**NP3
    S0=P1+P2
    S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
    RPALPX=AMAX1(-PALPO1,TPD*(S0-SQRT(S1)))
    IF(test_aeqb(PY,AH1P1))THEN
      IF(test_aeqb(PX,AAL1))THEN
        RHA0P1=RPALPX
      ELSEIF(test_aeqb(PX,AALO1))THEN
        RHA1P1=RPALPX
      ELSEIF(test_aeqb(PX,AALO2))THEN
        RHA2P1=RPALPX
      ELSEIF(test_aeqb(PX,AALO3))THEN
        RHA3P1=RPALPX
      ELSEIF(test_aeqb(PX,AALO4))THEN
        RHA4P1=RPALPX
      ENDIF
    ELSE
      IF(test_aeqb(PX,AAL1))THEN
        RHA0P2=RPALPX
      ELSEIF(test_aeqb(PX,AALO1))THEN
        RHA1P2=RPALPX
      ELSEIF(test_aeqb(PX,AALO2))THEN
        RHA2P2=RPALPX
      ELSEIF(test_aeqb(PX,AALO3))THEN
        RHA3P2=RPALPX
      ELSEIF(test_aeqb(PX,AALO4))THEN
        RHA4P2=RPALPX
      ENDIF
    ENDIF
!
!     IRON PHOSPHATE (STRENGITE)
!
    PX=AMAX1(AFE1,AFEO1,AFEO2,AFEO3,AFEO4)
    PY=AMAX1(AH1P1,AH2P1)
    R1=AHY1
    P3=AHY1
    IF(test_aeqb(PY,AH1P1))THEN
      P2=AH1P1
      IF(test_aeqb(PX,AFE1))THEN
        P1=AFE1
        NR1=1
        NP3=0
        SP=SHF0P1
      ELSEIF(test_aeqb(PX,AFEO1))THEN
        P1=AFEO1
        NR1=0
        NP3=0
        SP=SPF1P1
      ELSEIF(test_aeqb(PX,AFEO2))THEN
        P1=AFEO2
        NR1=0
        NP3=1
        SP=SHF2P1
      ELSEIF(test_aeqb(PX,AFEO3))THEN
        P1=AFEO3
        NR1=0
        NP3=2
        SP=SHF3P1
      ELSEIF(test_aeqb(PX,AFEO4))THEN
        P1=AFEO4
        NR1=0
        NP3=3
        SP=SHF4P1
      ENDIF
    ELSE
      P2=AH2P1
      IF(test_aeqb(PX,AFE1))THEN
        P1=AFE1
        NR1=2
        NP3=0
        SP=SHF0P2
      ELSEIF(test_aeqb(PX,AFEO1))THEN
        P1=AFEO1
        NR1=1
        NP3=0
        SP=SHF1P2
      ELSEIF(test_aeqb(PX,AFEO2))THEN
        P1=AFEO2
        NR1=0
        NP3=0
        SP=SPF2P2
      ELSEIF(test_aeqb(PX,AFEO3))THEN
        P1=AFEO3
        NR1=0
        NP3=1
        SP=SHF3P2
      ELSEIF(test_aeqb(PX,AFEO4))THEN
        P1=AFEO4
        NR1=0
        NP3=2
        SP=SHF4P2
      ENDIF
    ENDIF
    RHF0P1=0._r8
    RHF1P1=0._r8
    RHF2P1=0._r8
    RHF3P1=0._r8
    RHF4P1=0._r8
    RHF0P2=0._r8
    RHF1P2=0._r8
    RHF2P2=0._r8
    RHF3P2=0._r8
    RHF4P2=0._r8
    R1=AMAX1(ZERO,R1)
    P1=AMAX1(ZERO,P1)
    P2=AMAX1(ZERO,P2)
    P3=AMAX1(ZERO,P3)
    SPX=SP*R1**NR1/P3**NP3
    S0=P1+P2
    S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
    RPFEPX=AMAX1(-PFEPO1,TPD*(S0-SQRT(S1)))
    IF(test_aeqb(PY,AH1P1))THEN
      IF(test_aeqb(PX,AFE1))THEN
        RHF0P1=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO1))THEN
        RHF1P1=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO2))THEN
        RHF2P1=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO3))THEN
        RHF3P1=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO4))THEN
        RHF4P1=RPFEPX
      ENDIF
    ELSE
      IF(test_aeqb(PX,AFE1))THEN
        RHF0P2=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO1))THEN
        RHF1P2=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO2))THEN
        RHF2P2=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO3))THEN
        RHF3P2=RPFEPX
      ELSEIF(test_aeqb(PX,AFEO4))THEN
        RHF4P2=RPFEPX
      ENDIF
    ENDIF
!
!     DICALCIUM PHOSPHATE
!
    PX=AMAX1(AH1P1,AH2P1)
    R1=AHY1
    P1=ACA1
    IF(test_aeqb(PX,AH1P1))THEN
      P2=AH1P1
      NR1=0
      SP=SPCAD
    ELSEIF(test_aeqb(PX,AH2P1))THEN
      P2=AH2P1
      NR1=1
      SP=SHCAD2
    ENDIF
    RPCAD1=0._r8
    RHCAD2=0._r8
    R1=AMAX1(ZERO,R1)
    P1=AMAX1(ZERO,P1)
    P2=AMAX1(ZERO,P2)
    SPX=SP*R1**NR1
    S0=P1+P2
    S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
    RPCADX=AMAX1(-PCAPD1,TPD*(S0-SQRT(S1)))
    IF(test_aeqb(PX,AH1P1))THEN
      RPCAD1=RPCADX
    ELSEIF(test_aeqb(PX,AH2P1))THEN
      RHCAD2=RPCADX
    ENDIF
!
!     HYDROXYAPATITE
!
    PX=AMAX1(AH1P1,AH2P1)
    R1=AHY1
    P1=ACA1
    IF(test_aeqb(PX,AH1P1))THEN
      P2=AH1P1
      NR1=4
      SP=SHCAH1
    ELSEIF(test_aeqb(PX,AH2P1))THEN
      P2=AH2P1
      NR1=7
      SP=SHCAH2
    ENDIF
    RHCAH1=0._r8
    RHCAH2=0._r8
    R1=AMAX1(ZERO,R1)
    P1=AMAX1(ZERO,P1)
    P2=AMAX1(ZERO,P2)
    SPX=(SP*R1**NR1/P1**5)**0.333
    RPCAHX=AMAX1(-PCAPH1,TPD*(P2-SPX))
    IF(test_aeqb(PX,AH1P1))THEN
      RHCAH1=RPCAHX
    ELSEIF(test_aeqb(PX,AH2P1))THEN
      RHCAH2=RPCAHX
    ENDIF
    PALOH1=PALOH1+RPALOX
    PFEOH1=PFEOH1+RPFEOX
    PCACO1=PCACO1+RPCACX
    PCASO1=PCASO1+RPCASO
    PALPO1=PALPO1+RPALPX
    PFEPO1=PFEPO1+RPFEPX
    PCAPD1=PCAPD1+RPCADX
    PCAPH1=PCAPH1+RPCAHX
!
!     ANION EXCHANGE EQILIBRIA
!
    IF(VOLW(L,NY,NX).GT.ZEROS(NY,NX))THEN
      VOLWBK=AMIN1(1.0,BKVLX/VOLW(L,NY,NX))
    ELSE
      VOLWBK=1.0
    ENDIF
    IF(XAEC(L,NY,NX).GT.ZEROS(NY,NX))THEN
      RXOH2=TAD*(XOH11*AHY1-SXOH2*XOH21)/(XOH11+SPOH2)*VOLWBK
      RXOH1=TAD*(XOH01*AHY1-SXOH1*XOH11)/(XOH01+SPOH1)*VOLWBK
      SPH2P=SXH2P*DPH2O
      RXH2P=TAD*(XOH21*AH2P1-SPH2P*XH2P1)/(XOH21+SPH2P)*VOLWBK
      RYH2P=TAD*(XOH11*AH2P1-SXH2P*XH2P1*AOH1)/(XOH11+SXH2P*AOH1)*VOLWBK
!
!     HPO4 EXCHANGE
!
      SPH1P=SXH1P*DPH2O/DPH2P
      RXH1P=TAD*(XOH11*AH2P1-SPH1P*XH1P1)/(XOH11+SPH1P)*VOLWBK
      XOH01=XOH01-RXOH1
      XOH11=XOH11+RXOH1-RXOH2-RYH2P-RXH1P
      XOH21=XOH21+RXOH2-RXH2P
      XH1P1=XH1P1+RXH1P
      XH2P1=XH2P1+RXH2P+RYH2P
    ELSE
      RXOH2=0._r8
      RXOH1=0._r8
      RXH2P=0._r8
      RYH2P=0._r8
      RXH1P=0._r8
    ENDIF
!
!     CATION EXCHANGE
!
    IF(XCEC(L,NY,NX).GT.ZEROS(NY,NX))THEN
      AALX=AAL1**0.333
      AFEX=AFE1**0.333
      ACAX=ACA1**0.500
      AMGX=AMG1**0.500
      XCAX=CCEC/(1.0+GKC4(L,NY,NX)*AN41/ACAX &
      +GKCH(L,NY,NX)*AHY1/ACAX+GKCA(L,NY,NX)*AALX/ACAX &
      +GKCA(L,NY,NX)*AFEX/ACAX+GKCM(L,NY,NX)*AMGX/ACAX &
      +GKCN(L,NY,NX)*ANA1/ACAX+GKCK(L,NY,NX)*AKA1/ACAX)
      XN4Q=XCAX*AN41*GKC4(L,NY,NX)
      XHYQ=XCAX*AHY1*GKCH(L,NY,NX)
      XALQ=XCAX*AALX*GKCA(L,NY,NX)
      XFEQ=XCAX*AFEX*GKCA(L,NY,NX)
      XCAQ=XCAX*ACAX
      XMGQ=XCAX*AMGX*GKCM(L,NY,NX)
      XNAQ=XCAX*ANA1*GKCN(L,NY,NX)
      XKAQ=XCAX*AKA1*GKCK(L,NY,NX)
      XTLQ=XN4Q+XHYQ+XALQ+XFEQ+XCAQ+XMGQ+XNAQ+XKAQ
      IF(XTLQ.GT.ZERO)THEN
        FX=CCEC/XTLQ
      ELSE
        FX=0._r8
      ENDIF
      XN4Q=FX*XN4Q
      XHYQ=FX*XHYQ
      XALQ=FX*XALQ/3.0
      XFEQ=FX*XFEQ/3.0
      XCAQ=FX*XCAQ/2.0
      XMGQ=FX*XMGQ/2.0
      XNAQ=FX*XNAQ
      XKAQ=FX*XKAQ
      RXN4=TAD*AMIN1((XN4Q-XN41)*AN41/XN4Q,CN41)
      RXHY=TAD*AMIN1((XHYQ-XHY1)*AHY1/XHYQ,CHY1)
      RXAL=TAD*AMIN1((XALQ-XAL1)*AALX/XALQ,CAL1)
      RXFE=TAD*AMIN1((XFEQ-XFE1)*AFEX/XFEQ,CFE1)
      RXCA=TAD*AMIN1((XCAQ-XCA1)*ACAX/XCAQ,CCA1)
      RXMG=TAD*AMIN1((XMGQ-XMG1)*AMGX/XMGQ,CMG1)
      RXNA=TAD*AMIN1((XNAQ-XNA1)*ANA1/XNAQ,CNA1)
      RXKA=TAD*AMIN1((XKAQ-XKA1)*AKA1/XKAQ,CKA1)
      XN41=XN41+RXN4
      XHY1=XHY1+RXHY
      XAL1=XAL1+RXAL
      XFE1=XFE1+RXFE
      XCA1=XCA1+RXCA
      XMG1=XMG1+RXMG
      XNA1=XNA1+RXNA
      XKA1=XKA1+RXKA
    ELSE
      RXN4=0._r8
      RXHY=0._r8
      RXAL=0._r8
      RXFE=0._r8
      RXCA=0._r8
      RXMG=0._r8
      RXNA=0._r8
      RXKA=0._r8
    ENDIF
!
!     ORGANIC MATTER
!
    DP=DPCOH*DPALO
    XHC1=AHY1*(XCOOH-XALO21-XFEO21)/(AHY1+DPCOH)
    XALO21=AALO2*(XCOOH-XHC1)/(AALO2+DPALO)
    XFEO21=AFEO2*(XCOOH-XHC1)/(AFEO2+DPFEO)
    XCOO=AMAX1(0.0,XCOOH-XHC1-XALO21-XFEO21)
  ELSE
    RHAL1=0._r8
    RHALO1=0._r8
    RHALO2=0._r8
    RHALO3=0._r8
    RHALO4=0._r8
    RHFE1=0._r8
    RHFEO1=0._r8
    RHFEO2=0._r8
    RHFEO3=0._r8
    RHFEO4=0._r8
    RHCAC3=0._r8
    RHCACH=0._r8
    RHCACO=0._r8
    RPCACX=0._r8
    RPCASO=0._r8
    RPCADX=0._r8
    RPCAHX=0._r8
    RHA0P1=0._r8
    RHA1P1=0._r8
    RHA2P1=0._r8
    RHA3P1=0._r8
    RHA4P1=0._r8
    RHA0P2=0._r8
    RHA1P2=0._r8
